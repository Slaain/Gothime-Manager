<template>
  <div class="max-w-[720px] mx-auto">
    <div
        class="relative flex flex-col w-full h-full bg-white shadow-md text-slate-700 rounded-xl bg-clip-border"
    >
      <!-- Liste des groupes -->
      <div>
        <div class="flex justify-between items-center">
          <h3 class="text-lg font-semibold text-slate-800">Work Groups</h3>
          <button
              @click="openGroupModal"
              class="flex items-center gap-2 rounded bg-slate-800 py-2.5 px-4 text-xs font-semibold text-white shadow-md transition-all hover:shadow-lg"
          >
            <svg xmlns="http://www.w3.org/2000/svg" fill="currentColor" viewBox="0 0 24 24" class="w-4 h-4">
              <path d="M12 4v16m8-8H4" stroke-width="2" stroke="currentColor" fill="none" />
            </svg>
            Add Work Group
          </button>
        </div>

        <div v-if="groups.length > 0" class="mt-4">
          <table class="w-full text-left table-auto min-w-max">
            <thead>
            <tr>
              <th class="p-4 border-y border-slate-200 bg-slate-50">Group</th>
              <th class="p-4 border-y border-slate-200 bg-slate-50"></th>
              <th class="p-4 border-y border-slate-200 bg-slate-50"></th>
            </tr>
            </thead>
            <tbody>
            <tr
                v-for="(group, index) in groups"
                :key="index"
                :class="{'bg-gray-200': selectedGroup && selectedGroup.id === group.id}"
                class="cursor-pointer"
                @click="selectGroup(group)"
            >
              <td class="p-4 border-b border-slate-200">{{ group.name }}</td>
              <td class="p-4 border-b border-slate-200">
                <button class="text-sm text-green-600 hover:underline">
                  Select
                </button>
              </td>
              <td class="p-4 border-b border-slate-200">
                <button @click.stop="deleteGroup(group.id)" class="text-sm text-red-600 hover:underline">
                  Delete
                </button>
              </td>
            </tr>
            </tbody>
          </table>
        </div>
      </div>

      <!-- Détails du groupe sélectionné et gestion des utilisateurs -->
      <div v-if="selectedGroup" class="pl-5 pr-4">
        <h3 class="text-lg font-semibold text-slate-800">
          Group: {{ selectedGroup.name }}
        </h3>
        <div class="mt-4">
          <h4 class="text-slate-700">Users in this Group</h4>
          <ul>
            <li
                v-for="(user, index) in selectedGroupUsers"
                :key="index"

                class="flex justify-between items-center border-b p-2"
            >
              <span>{{ user.username }} ({{ user.email }})</span>
              <button @click="removeUser(user.id)" class="text-sm text-red-600 hover:underline">Delete</button>
            </li>
          </ul>
        </div>

        <div class="flex justify-between items-center mt-4 pr-12-7 pb-6">
          <h4 class="text-slate-700">Add User</h4>
          <button @click="openUserModal" class="text-sm text-blue-600 hover:underline">Add User</button>
        </div>
      </div>

      <!-- Modale de création de groupe -->
      <div v-if="showGroupModal" class="fixed inset-0 z-50 flex items-center justify-center bg-black bg-opacity-50">
        <div class="w-1/2 px-8 py-4 bg-white rounded-lg shadow-md">
          <h3 class="text-lg font-semibold text-slate-800 mb-4">Create Work Group</h3>
          <label class="block text-sm font-bold mt-2">Group Name</label>
          <input v-model="newGroup.name" type="text" class="w-full p-2 border border-gray-300 rounded" />

          <label class="block text-sm font-bold mt-2">Start Date and Time</label>
          <input v-model="newGroup.start_date" type="datetime-local" class="w-full p-2 border border-gray-300 rounded" />

          <label class="block text-sm font-bold mt-2">End Date and Time</label>
          <input v-model="newGroup.end_date" type="datetime-local" class="w-full p-2 border border-gray-300 rounded" />

          <div class="flex justify-end gap-4 mt-4">
            <button @click="createGroup" class="bg-blue-500 text-white px-4 py-2 rounded hover:bg-blue-600">
              Create
            </button>
            <button @click="closeGroupModal" class="bg-red-500 text-white px-4 py-2 rounded hover:bg-red-600">
              Cancel
            </button>
          </div>
        </div>
      </div>

      <!-- Modale d'ajout d'utilisateur -->
      <div v-if="showUserModal" class="fixed inset-0 z-50 flex items-center justify-center bg-black bg-opacity-50">
        <div class="w-1/2 px-8 py-4 bg-white rounded-lg shadow-md">
          <h3 class="text-lg font-semibold text-slate-800 mb-4">Add User to Group</h3>
          <Dropdown :users="allUsers" @onUserSelected="setSelectedUser" />
          <div class="flex justify-end gap-4 mt-4">
            <button @click="addUserToGroup" class="bg-green-500 text-white px-4 py-2 rounded hover:bg-green-600">
              Add
            </button>
            <button @click="closeUserModal" class="bg-red-500 text-white px-4 py-2 rounded hover:bg-red-600">
              Cancel
            </button>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import Dropdown from './Dropdown.vue';
import axios from 'axios';

export default {
  components: {
    Dropdown,
  },
  data() {
    return {
      groups: [],
      allUsers: [],
      newGroup: {
        name: "",
        start_date: "",
        end_date: "",
      },
      selectedGroup: null,
      selectedGroupUsers: [],
      selectedUser: null,
      showGroupModal: false,
      showUserModal: false,
    };
  },
  methods: {
    async selectGroup(group) {
      this.selectedGroup = group;
      try {
        const response = await axios.get(`http://localhost:4000/api/groups/${group.id}`);
        this.selectedGroupUsers = response.data.data.users || [];
      } catch (error) {
        console.error("Erreur lors de la récupération des utilisateurs du groupe :", error);
      }
    },
    async addUserToGroup() {
      if (!this.selectedUser) {
        console.error('Aucun utilisateur sélectionné');
        return;
      }
      try {
        await axios.post(
            `http://localhost:4000/api/groups/${this.selectedGroup.id}/users/${this.selectedUser}`,
            {},
            { headers: { 'Content-Type': 'application/json' } }
        );
        this.selectGroup(this.selectedGroup);
        this.closeUserModal();
      } catch (error) {
        console.error("Erreur lors de l'ajout d'un utilisateur :", error);
      }
    },
    openGroupModal() {
      this.showGroupModal = true;
    },
    closeGroupModal() {
      this.showGroupModal = false;
    },
    openUserModal() {
      this.showUserModal = true;
    },
    closeUserModal() {
      this.showUserModal = false;
    },
    async createGroup() {
      try {
        const response = await axios.post(
            'http://localhost:4000/api/groups',
            { group: this.newGroup },
            { headers: { 'Content-Type': 'application/json' } }
        );
        this.groups.push(response.data.group);
        this.newGroup = { name: "", start_date: "", end_date: "" };
        this.closeGroupModal();
      } catch (error) {
        console.error("Erreur lors de la création du groupe :", error);
      }
    },
    async deleteGroup(id) {
      try {
        await axios.delete(`http://localhost:4000/api/groups/${id}`);
        this.groups = this.groups.filter((group) => group.id !== id);
      } catch (error) {
        console.error("Erreur lors de la suppression du groupe :", error);
      }
    },
    setSelectedUser(user) {
      this.selectedUser = user;
    },
    async removeUser(userId) {
      try {
        await axios.delete(`http://localhost:4000/api/groups/${this.selectedGroup.id}/users/${userId}`);
        this.selectedGroupUsers = this.selectedGroupUsers.filter((user) => user.id !== userId);
      } catch (error) {
        console.error("Erreur lors de la suppression de l'utilisateur :", error);
      }
    },
  },
  async mounted() {
    try {
      const response = await axios.get("http://localhost:4000/api/groups");
      this.groups = response.data.data;
      const usersResponse = await axios.get('http://localhost:4000/api/users');
      this.allUsers = usersResponse.data.users || [];
    } catch (error) {
      console.error("Erreur lors de la récupération des données :", error);
    }
  },
};
</script>
